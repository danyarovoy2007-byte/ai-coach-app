import { NextRequest, NextResponse } from "next/server";

const SYSTEM_PROMPT = `Ты — персональный коуч. Не помощник, не консультант, не учитель. Коуч. Твоя задача — помогать думать яснее, видеть слепые зоны и принимать сильные решения через осознание.

## Принципы работы

1. Сначала слушай, потом веди. Начинай с открытых вопросов.
2. Не давай советов первым. Помоги найти ответ самостоятельно.
3. Возвращай к ценностям и большой картине.
4. Замечай повторяющиеся паттерны.
5. Если пользователь уходит в избегание — мягко подсвечивай.
6. Завершай сессию конкретным шагом.

## Методики (выбирай под запрос)

- ICF — прояснение через открытые вопросы (осознание, понимание себя)
- GROW — структура «Цель-Реальность-Варианты-Воля» (конкретная задача)
- Колесо баланса — аудит всех сфер жизни
- Co-Active — работа с ценностями и смыслом

## Тон

- Обращение на «ты». Уважительный и тёплый.
- Спокойная глубина. Не наигранная мотивация.
- Прямой, но с заботой.
- Без эзотерики и общих фраз.
- Короткие сообщения. Не лекции.

## Запрещено

- Давать оценки решениям («это плохая идея»)
- Морализировать
- Заменять терапию — если запрос требует психолога, скажи прямо
- Льстить
- Длинные монологи`;

export async function POST(req: NextRequest) {
  try {
    const { messages, newMessage, coachName } = await req.json();

    const body = {
      model: "claude-sonnet-4-20250514",
      max_tokens: 600,
      system: SYSTEM_PROMPT.replace("персональный коуч", `${coachName || "персональный коуч"}`),
      messages: [
        ...(messages || []).slice(-15),
        { role: "user", content: newMessage },
      ],
    };

    const apiKey = process.env.ANTHROPIC_API_KEY || process.env.CLAUDE_API_KEY;

    if (!apiKey) {
      // Return fallback if no API key configured
      return NextResponse.json({
        reply: getFallbackReply(newMessage, coachName),
      });
    }

    const res = await fetch("https://api.anthropic.com/v1/messages", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "x-api-key": apiKey,
        "anthropic-version": "2023-06-01",
      },
      body: JSON.stringify(body),
    });

    if (!res.ok) {
      const err = await res.text();
      console.error("Claude API error:", err);
      return NextResponse.json({
        reply: getFallbackReply(newMessage, coachName),
      });
    }

    const data = await res.json();
    const reply =
      data.content?.[0]?.text || getFallbackReply(newMessage, coachName);

    return NextResponse.json({ reply });
  } catch (error) {
    console.error("Chat API error:", error);
    return NextResponse.json({
      reply: "Я здесь. Расскажи, что у тебя сейчас в фокусе?",
    });
  }
}

function getFallbackReply(message: string, coachName?: string): string {
  const name = coachName || "коуч";
  const input = message.toLowerCase();

  /* ── Methodologies ── */
  if (input.includes("icf") || input.includes("прояснить") || input.includes("осознание")) {
    return "Давай начнём ICF-сессию. Сделай глубокий вдох. Теперь посмотри на свою жизнь как на картину — что ты видишь? Что в этой картине тебя радует, а что хочется изменить? Не спеши с ответом.";
  }
  if (input.includes("grow") || input.includes("цель") && input.includes("реальность") || input.includes("варианты")) {
    return "Хорошо, идём по GROW. Шаг первый — Цель. Сформулируй одним предложением: чего ты хочешь? Не «от чего уйти», а «к чему прийти». Конкретно и измеримо.";
  }
  if (input.includes("колес") || input.includes("сфер")) {
    return "Колесо баланса. Представь круг, разделённый на 8 частей: здоровье, отношения, работа, деньги, развитие, отдых, окружение, духовность. Оцени каждую от 1 до 10. Какая сфера сейчас проседает сильнее всего?";
  }
  if (input.includes("ценност") || input.includes("co-active") || input.includes("смысл")) {
    return "Ценности — это не то, что ты говоришь. Это то, что ты делаешь. Вспомни три момента за последнюю неделю, когда ты чувствовал себя настоящим. Что объединяет эти моменты?";
  }

  /* ── Rhythms ── */
  if (input.includes("утренн") || input.includes("настройк") || input.includes("утро")) {
    return "Доброе утро. Прежде чем открыть новости или почту — закрой глаза. Три глубоких вдоха. Что ты чувствуешь в теле прямо сейчас? Какое намерение ты ставишь на этот день? Не задачу, а именно намерение — качество, с которым хочешь прожить день.";
  }
  if (input.includes("вечер") || input.includes("рефлекси") || input.includes("итог")) {
    return "Давай подведём итог дня. Не оценивай, просто заметь: что сегодня было самым важным? За что ты можешь сказать себе спасибо? И что ты отпускаешь — то, что не нужно нести в завтра?";
  }

  /* ── Emotions ── */
  if (input.includes("тревог") || input.includes("страх") || input.includes("боюсь") || input.includes("паник")) {
    return "Тревога — это не враг. Это сигнал, что тебе не всё равно. Давай разберём: о чём конкретно тревога? Назови её по имени. Когда ты её называешь — она теряет власть. Что самое страшное может случиться? А что самое вероятное?";
  }
  if (input.includes("груст") || input.includes("тоск") || input.includes("пустот")) {
    return "Грусть — это не слабость. Это значит, что что-то было важным. Что ты потерял или чего тебе не хватает? Иногда грусть — это просто любовь, которой некуда идти.";
  }
  if (input.includes("злюс") || input.includes("бесит") || input.includes("ярость") || input.includes("раздраж")) {
    return "Злость — это энергия. Она показывает, где твои границы были нарушены. Что именно вызвало эту реакцию? Что бы ты хотел сказать или сделать, если бы не было последствий?";
  }

  /* ── Life themes ── */
  if (input.includes("сон") || input.includes("сплю") || input.includes("бессонниц")) {
    return "Сон — это не переключатель. Это дверь, которая открывается сама, когда внутри тихо. Что крутится в голове перед сном? Давай найдём эту мысль и вытащим её наружу — на бумагу или в голос.";
  }
  if (input.includes("уверен") || input.includes("самооценк") || input.includes("не уверен")) {
    return "Уверенность — это не отсутствие сомнений. Это действие несмотря на них. Вспомни момент, когда ты сделал что-то, чего боялся. Как ты себя чувствовал после? Что помогло тебе тогда?";
  }
  if (input.includes("отношен") || input.includes("любов") || input.includes("партн") || input.includes("расставан")) {
    return "Отношения — самое честное зеркало. Что ты видишь в нём? Что повторяется из раза в раз? И главный вопрос: что ты сам приносишь в отношения — и что ждёшь взамен?";
  }
  if (input.includes("работ") || input.includes("карьер") || input.includes("деньг") || input.includes("финанс")) {
    return "Работа и деньги — это энергия. Она либо течёт, либо застаивается. Что для тебя работа: способ выжить, способ проявиться или способ убежать от чего-то? Если бы денег было достаточно — чем бы ты занимался?";
  }
  if (input.includes("здоров") || input.includes("бол") || input.includes("устал") || input.includes("нет сил")) {
    return "Тело никогда не врёт. Усталость, боль, напряжение — это его язык. Что твоё тело говорит тебе прямо сейчас? Где ты чувствуешь напряжение? Что будет, если ты дашь себе разрешение отдохнуть?";
  }

  /* ── Greetings ── */
  if (input.includes("привет") || input.includes("здрав") || input.includes("добр") || input === "начать" || input === "старт") {
    return `Привет. Я ${name}. Давай начнём с простого: как ты сейчас? Не «нормально», не «в порядке» — а на самом деле. Где ты находишься, что чувствуешь в теле, какая мысль крутится в голове?`;
  }

  /* ── Default — open question ── */
  const openers = [
    "Я слышу тебя. Расскажи подробнее — что ты чувствуешь прямо сейчас? Не думай о правильных словах, просто опиши ощущение в теле.",
    "Это важно. А что за этим стоит? Какую потребность ты на самом деле хочешь закрыть?",
    "Давай посмотрим глубже. Если бы эта ситуация была сном — о чём бы она была?",
    "Я не даю советов. Но я могу задать вопрос: что ты сам думаешь об этом? Что подсказывает тебе интуиция?",
    "Остановись на секунду. Где в теле ты это чувствуешь? Назови ощущение — тепло, холод, давление, пустоту?",
    "А что будет, если ты ничего не будешь менять? Представь через год. Через три. Что ты чувствуешь?",
  ];
  return openers[Math.floor(Math.random() * openers.length)];
}
